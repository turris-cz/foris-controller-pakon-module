import json
import logging
import random
import string

from foris_controller.handler_base import BaseMockHandler
from foris_controller.utils import logger_wrapper

from .. import Handler

logger = logging.getLogger(__name__)


class MockPakonHandler(Handler, BaseMockHandler):
    LENGTH = 1024 * 1024  # always return big data

    @logger_wrapper(logger)
    def perform_query(self, data):
        data = "".join(random.choice(string.ascii_letters) for _ in range(MockPakonHandler.LENGTH))
        return json.dumps({"res": data})
